import GeneralError from './error';

const handleErrors = (err, req, res) => {
  if (err instanceof GeneralError) {
    return res.status(err.statusCode).json({
      status: 'error',
      message: err.message,
    });
  }

  return res.status(500).json({
    status: 'error',
    message: err.message,
  });
};

export default handleErrors;
